package test;

/**
 * This class stores 2 double values
 * @author HCI Lab
 *
 */
public class Coordinate {
	public double x;
	public double y;
	public Coordinate(double x, double y) {
		this.x = x;
		this.y = y;
	}
	
	public void setX(double x) {
		this.x = x;
	}
	public void setY(double x) {
		this.y = x;
	}

}
